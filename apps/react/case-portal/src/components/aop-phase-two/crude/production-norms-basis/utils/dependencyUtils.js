/**
 * Handle addition dependency (sum of multiple fields)
 * @param {Object} params - Parameters object
 * @param {string} params.currentFieldName - The name of the field being changed
 * @param {number} params.value - The new value for the field
 * @param {Object} params.dependencyConfig - The dependency configuration
 * @param {Array} params.rows - Current rows data
 * @param {Function} params.setRowsCallback - Callback to update rows
 * @param {Function} params.setModifiedCells - Callback to update modified cells
 * @param {Function} params.setCustomModifiedCells - Callback to update custom modified cells
 */
export const handleAdditionDependency = ({
  currentFieldName,
  value,
  dependencyConfig,
  rows,
  setRowsCallback,
  setModifiedCells,
  setCustomModifiedCells,
}) => {
  const { sourceFields, targetField } = dependencyConfig

  // Validate that current field is one of the source fields
  if (!sourceFields.includes(currentFieldName)) return

  // Get values from all source fields
  const sourceValues = sourceFields.map((fieldName) => {
    if (fieldName === currentFieldName) {
      return parseFloat(value) || 0
    }
    const sourceRow = rows.find(
      (r) => r.displayName === fieldName || r.name === fieldName,
    )
    return parseFloat(sourceRow?.attributeValue) || 0
  })

  // Calculate sum
  const sum = sourceValues.reduce((acc, val) => acc + val, 0)

  // Find target row
  const targetRow = rows.find(
    (r) => r.displayName === targetField || r.name === targetField,
  )
  if (!targetRow) {
    console.warn(`Target field not found: ${targetField}`)
    return
  }

  // Generate auto-remark
  const autoRemark = `Auto-calculated: Sum of ${sourceFields.join(' + ')}`

  // Preserve existing user remarks if they exist and are not auto-generated
  const existingRemark = targetRow.remarks || ''
  const isAutoGenerated = existingRemark.startsWith('Auto-calculated:')
  const finalRemark =
    isAutoGenerated || !existingRemark
      ? autoRemark
      : `${existingRemark} | ${autoRemark}`

  // Update rows
  setRowsCallback((prevRows) => {
    return prevRows.map((row) => {
      if (row.displayName === targetField || row.name === targetField) {
        return {
          ...row,
          attributeValue: sum,
          remarks: finalRemark,
          inEdit: true,
        }
      }
      return row
    })
  })

  // Update modifiedCells
  setModifiedCells((prev) => {
    return {
      ...prev,
      [targetRow.id]: {
        ...targetRow,
        attributeValue: sum,
        remarks: finalRemark,
        inEdit: true,
      },
    }
  })

  // Update customModifiedCells for orange highlighting
  setCustomModifiedCells((prev) => {
    return {
      ...prev,
      [targetRow.id]: {
        ...(prev[targetRow.id] || {}),
        attributeValue: sum,
        remarks: finalRemark,
      },
    }
  })
}
