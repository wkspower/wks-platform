package com.wks.caseengine.rest.model;

public class FunctionalLocation {
	private String id;
	private String assetFL;
	private String assetNo;
	private String assetSortFeild;
	private String parentFLName;
	private String uasAssetName;
	private String usaDisplayName;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getAssetFL() {
		return assetFL;
	}
	public void setAssetFL(String assetFL) {
		this.assetFL = assetFL;
	}
	public String getAssetNo() {
		return assetNo;
	}
	public void setAssetNo(String assetNo) {
		this.assetNo = assetNo;
	}
	public String getAssetSortFeild() {
		return assetSortFeild;
	}
	public void setAssetSortFeild(String assetSortFeild) {
		this.assetSortFeild = assetSortFeild;
	}
	public String getParentFLName() {
		return parentFLName;
	}
	public void setParentFLName(String parentFLName) {
		this.parentFLName = parentFLName;
	}
	public String getUasAssetName() {
		return uasAssetName;
	}
	public void setUasAssetName(String uasAssetName) {
		this.uasAssetName = uasAssetName;
	}
	public String getUsaDisplayName() {
		return usaDisplayName;
	}
	public void setUsaDisplayName(String usaDisplayName) {
		this.usaDisplayName = usaDisplayName;
	}
}
